    @Override
    public void update(Observable o, Object arg) {
        RawData receiveData = (RawData) arg;
        hanlderResponse(receiveData);
    }

    private void hanlderResponse(RawData receiveData) {
        String code = receiveData.getCode();
        switch(code) {
            case Resources.SIGNUP_SUCCESS:
                System.out.println(Resources.SIGNUP_SUCCESS + " " + receiveData.getData());
                JOptionPane.showMessageDialog(this, "Đăng ký thành công");
                break;
            case Resources.SIGNUP_FAIL:
                System.out.println(Resources.SIGNUP_FAIL + " " + receiveData.getData());
                JOptionPane.showMessageDialog(this, "Đăng ký thất bại");
                break;
        }
    }
