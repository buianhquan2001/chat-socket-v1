    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        String name = txtDisplayName.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String passwordConfirm = txtPassConfirm.getText();
        if (name.length() == 0 || username.length() == 0 || password.length() == 0 || passwordConfirm.length() == 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng");
            return ;
        }
        if (!password.equals(passwordConfirm)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu xác nhận không trùng khớp");
            return ;
        }
        SignUp signUp = new SignUp();
        signUp.setName(name);
        signUp.setPassword(password);
        signUp.setUsername(username);
        clientHandler.register(signUp);
    }//GEN-LAST:event_btnSignupActionPerformed

